import matplotlib.pyplot as plt

# Defined numbers for plotting the graph
x_values = [1, 2, 3, 4, 5]
y_values = [2, 4, 6, 8, 10]

# Plotting the graph
plt.plot(x_values, y_values)

# Adding title and axis labels
plt.title('Example Graph')
plt.xlabel('X-axis')
plt.ylabel('Y-axis')

# Displaying the graph
plt.show()
